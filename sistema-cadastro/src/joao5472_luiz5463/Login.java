    private void btnLogarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLogarActionPerformed
        boolean validarUsuario = false, validarSenha = false;
        
        Conexao con;
        
        try
        {
            con = new Conexao();
            con.Conectar();

            if (txtUsuario_Login.getText().equals("") || txtSenha_Login.getText().equals(""))
            {
                JOptionPane.showMessageDialog(null, "Preencha todos os campos");
            }
            else
            {
                ResultSet usuario = con.Consultar("select cd_usuario from tb_usuario");
                
                while(usuario.next())
                {
                    String user = usuario.getString("cd_usuario");
                    if (user.equals(txtUsuario_Login.getText()))
                    {
                        validarUsuario = true;
                        break;
                    }
                }
                
                ResultSet senha = con.Consultar("select cd_senha from tb_usuario");
                while(senha.next())
                {
                    String password = senha.getString("cd_senha");
                    if (password.equals(txtSenha_Login.getText()))
                    {
                        validarSenha = true;
                        break;
                    }
                }
                
                if (validarSenha == true && validarUsuario == true)
                {
                    JOptionPane.showMessageDialog(null, "Login realizado com sucesso.");
                    txtUsuario_Login.setText("");
                    txtSenha_Login.setText("");
                    this.setVisible(false);
                    jCadastro.setSize(538,700);
                    jCadastro.setTitle("Cadastro de Usuarios");
                    jCadastro.setModal(true);
                    jCadastro.setLocationRelativeTo(null);
                    jCadastro.setVisible(true);
                } 
                else
                {
                    JOptionPane.showMessageDialog(null, "Digite os dados de login corretamente.");
                }
            }
        }
        catch(ClassNotFoundException ex)
        {
            Logger.getLogger(Banco.class.getName()).log(Level.SEVERE, null, ex);
        }
        catch(SQLException ex)
        {
            Logger.getLogger(Banco.class.getName()).log(Level.SEVERE, null, ex);
        }
        catch(NullPointerException ex)
        {
            Logger.getLogger(Banco.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnLogarActionPerformed

